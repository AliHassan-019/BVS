void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart == &huart1)  
    {
        
        VP_Address = (rx_buffer[4] << 8) | rx_buffer[5];
        
        if (VP_Address == 0x1000)
        {
            switch(rx_buffer[8])
            {
                case 0:
									HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_1);
									TIM1->CCR1=stepper_speed*0.2;
									HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
									HAL_GPIO_WritePin(STEP_GPIO_PORT, DIR_PIN, GPIO_PIN_SET);
									break;  
                case 1:
									HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_1);
									TIM1->CCR1=stepper_speed*0.2;
									HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
                  HAL_GPIO_WritePin(STEP_GPIO_PORT, DIR_PIN, GPIO_PIN_RESET);
                   break;  
            }
        }
        else if (VP_Address == 0x2000)
        {
            switch(rx_buffer[8])
            {
                case 0:
                stepper_speed = 62500;
								//adjust_stepper_speed(stepper_speed);
								//stepper_speed = 500;
								adjust_stepper_speed(stepper_speed);
                break;  
                case 1:
                stepper_speed = 50000;
								//stepper_speed = 1500;
								adjust_stepper_speed(stepper_speed);
                   break;  
                case 2:
                stepper_speed = 41666;
								//stepper_speed = 5000;
								adjust_stepper_speed(stepper_speed);
                  break;  
            }
        }
        
    }
}




